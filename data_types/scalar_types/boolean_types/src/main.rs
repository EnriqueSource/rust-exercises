fn main() {
    // without explicit type annotation
    let t = true;
    let f = false;
    println!("The value of t is: {t}");
    println!("The value of f is: {f}");
    // with explicit annotation
    let t1: bool = true;
    let f1: bool = false;
    println!("The value of t1 is: {t1}");
    println!("The value of f1 is: {f1}");
}
